						SELECT STUFF(
									(SELECT '|' + HNO.NOTE_TEXT
									 FROM (
											SELECT /* Remove duplicates, but preserve order of entries. */
												 LTRIM(RTRIM(REPLACE(REPLACE(REPLACE(NOTE_TEXT, CHAR(13)/* CR */, '\N'), CHAR(10) /* LF */, ''), CHAR(9) /* TAB */, '\T'))) AS NOTE_TEXT
