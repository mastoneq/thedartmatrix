CREATE TABLE #FinalNotebyCSN ( [NOTE_ID] varchar(254), [CONTACT_SERIAL_NUM] decimal(18,0), [UPDATE_USER_ID] VARCHAR(18)
							, [NOTE_STATUS_C] VARCHAR(66), [UPD_AUT_LOCAL_DTTM] DATETIME, [NUMBER_CHANGES] INT, [AUTHOR_SERVICE_C] VARCHAR(66) )

INSERT #FinalNotebyCSN (NOTE_ID, CONTACT_SERIAL_NUM, UPDATE_USER_ID, NOTE_STATUS_C, UPD_AUT_LOCAL_DTTM, NUMBER_CHANGES, AUTHOR_SERVICE_C)
SELECT D.NOTE_ID, D.CONTACT_SERIAL_NUM, D.UPDATE_USER_ID, D.NOTE_STATUS_C, D.UPD_AUT_LOCAL_DTTM, D.NUMBER_CHANGES, D.AUTHOR_SERVICE_C
FROM (
		SELECT 
		D.NOTE_ID
		, N.CONTACT_SERIAL_NUM
		, ROW_NUMBER() OVER (PARTITION BY N.NOTE_ID ORDER BY N.CONTACT_DATE_REAL DESC)	AS seq
		,N.UPDATE_USER_ID
		,N.NOTE_STATUS_C
		,N.UPD_AUT_LOCAL_DTTM
		,OA.NUMBER_CHANGES  
		,N.AUTHOR_SERVICE_C
		FROM #DATA AS D
		INNER JOIN clarity.dbo.NOTE_ENC_INFO AS N ON N.NOTE_ID = D.NOTE_ID 
			OUTER APPLY(
					SELECT COUNT(DISTINCT E.CONTACT_date_REAL) AS NUMBER_CHANGES     --if he does want all types of status counted, would be more efficient to use max( CONTACT_NUM) in NOTE_ENC_INFO
					FROM clarity.dbo.NOTE_ENC_INFO AS E
					WHERE
					E.NOTE_ID = N.NOTE_ID
					

			) AS OA
		WHERE
		N.NOTE_STATUS_C IN ('2', '3') --signed, adendum
		) AS D 
WHERE D.seq = 1 
GROUP BY D.NOTE_ID, D.CONTACT_SERIAL_NUM, D.UPDATE_USER_ID, D.NOTE_STATUS_C, D.UPD_AUT_LOCAL_DTTM, D.NUMBER_CHANGES, D.AUTHOR_SERVICE_C

