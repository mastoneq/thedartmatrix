											FROM CLARITY.dbo.HNO_NOTE_TEXT  AS HNO
											WHERE 
												(HNO.NOTE_CSN_ID = D.CONTACT_SERIAL_NUM) 
											GROUP BY
												  LTRIM(RTRIM(REPLACE(REPLACE(REPLACE(NOTE_TEXT, CHAR(13)/* CR */, '\N'), CHAR(10) /* LF */, ''), CHAR(9) /* TAB */, '\T')))
									) AS HNO
									ORDER BY
										HNO.LINE
									FOR XML PATH('')), 1, 1, ''
									) AS NOTE_TEXT	
				) AS OA


